    private void btnSacarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacarActionPerformed
        // TODO add your handling code here:
        String valorDigitado = txtValor.getText();
        Double valorSacar = Double.valueOf(valorDigitado);
        
        Boolean temSaldo = conta1.sacar(valorSacar);

        if (temSaldo) {
            lblResultado1.setText(conta1.getSaldo().toString());
            lblCondicao1.setText("Saque bem sucedido!");
            lblCondicao1.setForeground(Color.green);

        } else {
            lblCondicao1.setText("Saldo insuficiente");
            lblCondicao1.setForeground(Color.red);

        }


    }//GEN-LAST:event_btnSacarActionPerformed

    private void btnDepositarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDepositarActionPerformed
        // TODO add your handling code here:
        String valorDigitado = txtValor.getText();
        Double valorDeposito = Double.valueOf(valorDigitado);
        conta1.depositar(valorDeposito);

        lblResultado1.setText(conta1.getSaldo().toString());
    }//GEN-LAST:event_btnDepositarActionPerformed

